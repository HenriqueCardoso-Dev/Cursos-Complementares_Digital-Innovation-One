let d = new Date(); // Guardando o valor da Data dentro da variável.

alert(d.getMonth()+1); //saida do valor do mês armazenado na variável anteriormente declarada, o "+1", serve para deixar o código com uma boa resposta, já que o getMonth sempre começa a contar os meses a partir do 0 e não do 1 como costumamos fazer!

alert(d.getDate()); // saída do dia

alert(d.getHours()); //saída das horas

alert(d.getMinutes()); //saída dos minutos

